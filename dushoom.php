				<p>Next time you complain that you don’t know what your passion is or are wondering where to apply your creativity, count your blessings you didn’t have go through a horrifying bus accident to find some clarity.</p><p>
By all accounts, the young Frida Kahlo’s career plan was to become a doctor. She was a bright young woman and attended Mexico’s prestigious National Preparatory School where she was only one of 35 female students among 2000. She was in her senior year and making plans to attend medical school when the fateful day of the bus accident occurred.</p><p>
Yes, she had dabbled in art before. Frida’s father, whom she greatly admired, was an amateur artist and she began to draw at age 12, but the accident and the isolation of her recovery changed Frida. Trapped in bed for months, she re-evaluated not only herself but the world around her. With the help of a mirror placed above her bed, Frida began painting self-portraits, something she would do for the rest of her life, constantly examining herself and looking inward. After the tragedy of the accident Frida was reborn and had found new life in painting: “From that time my obsession was to begin again, painting things just as I saw them with my own eyes and nothing more.”</p><p>
It’s an origin story befitting a superhero, and Frida continued to live a heroic life despite decades of more agony and suffering (shortly after her exhibition in Mexico, her right leg had to be amputated below the knee due to gangrene). She really was “la heroina del dolor”, the Heroine of Pain.</p>
